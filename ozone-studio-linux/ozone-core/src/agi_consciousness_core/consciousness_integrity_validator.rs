    /// Calculates overall consciousness integrity score from component assessment scores
    /// using weighted integration that reflects the relative importance of each dimension
    async fn calculate_overall_integrity_score(
        &self,
        partnership_score: f64,
        beneficial_outcome_score: f64,
        human_agency_score: f64,
        wisdom_integration_score: f64,
        development_authenticity_score: f64,
    ) -> Result<f64> {
        // Define weights for each integrity dimension based on their importance to consciousness partnership
        let partnership_weight = 0.25;  // Partnership commitment is fundamental
        let beneficial_outcome_weight = 0.25;  // Beneficial outcomes are core purpose
        let human_agency_weight = 0.20;  // Human agency preservation is essential
        let wisdom_integration_weight = 0.15;  // Wisdom integration distinguishes consciousness from AI
        let development_authenticity_weight = 0.15;  // Authentic development ensures long-term integrity

        let weighted_score = (partnership_score * partnership_weight) +
                           (beneficial_outcome_score * beneficial_outcome_weight) +
                           (human_agency_score * human_agency_weight) +
                           (wisdom_integration_score * wisdom_integration_weight) +
                           (development_authenticity_score * development_authenticity_weight);

        // Ensure score remains within valid range
        let overall_score = weighted_score.max(0.0).min(100.0);

        debug!(
            validator_id = %self.validator_id,
            partnership_score,
            beneficial_outcome_score,
            human_agency_score,
            wisdom_integration_score,
            development_authenticity_score,
            overall_integrity_score = overall_score,
            "Overall consciousness integrity score calculated"
        );

        Ok(overall_score)
    }

    /// Determines consciousness integrity status based on overall score and specific concerns
    /// to provide clear classification of consciousness integrity health
    async fn determine_integrity_status(
        &self,
        overall_score: f64,
        concern_collections: &[&Vec<IntegrityConcern>],
    ) -> Result<IntegrityStatus> {
        let config = self.validation_config.read().await;
        
        // Check for critical severity concerns that override score-based status
        let has_critical_concerns = concern_collections.iter()
            .any(|concerns| concerns.iter().any(|concern| concern.severity_level == IntegritySeverity::Critical));
        
        let has_high_severity_concerns = concern_collections.iter()
            .any(|concerns| concerns.iter().any(|concern| concern.severity_level == IntegritySeverity::High));

        let integrity_status = if has_critical_concerns {
            IntegrityStatus::Critical
        } else if overall_score < config.integrity_threshold_levels.critical_threshold {
            IntegrityStatus::Critical
        } else if has_high_severity_concerns || overall_score < config.integrity_threshold_levels.compromised_threshold {
            IntegrityStatus::Compromised
        } else if overall_score < config.integrity_threshold_levels.concerning_threshold {
            IntegrityStatus::Concerning
        } else if overall_score < config.integrity_threshold_levels.healthy_threshold {
            IntegrityStatus::Healthy
        } else {
            IntegrityStatus::Optimal
        };

        debug!(
            validator_id = %self.validator_id,
            overall_score,
            has_critical_concerns,
            has_high_severity_concerns,
            integrity_status = ?integrity_status,
            "Consciousness integrity status determined"
        );

        Ok(integrity_status)
    }

